    private void buttonAjoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAjoutActionPerformed
        // TODO add your handling code here:
         try{
          connexion();  
          String dataMarque=textMarque.getText(); 
          String dataPrixUnit=textPrixUnit.getText();
          String dataPrixPack=textPrixPack.getText();
          String dataQuantitePack=textQuantitePack.getText();
          String dataStock=textStock.getText();
          String dataDescription=textDescription.getText();
          
          String requeteInsertionArticle="INSERT INTO article(marque,prix_unitaire,prix_pack,Quantite_pack,reference,stock,description) VALUES(?,?,?,?,NULL,?,?)";
          
          pst=con.prepareStatement(requeteInsertionArticle);
          pst.setString(1,dataMarque);
          pst.setString(2,dataPrixUnit);
          pst.setString(3,dataPrixPack);
          pst.setString(4,dataQuantitePack);
          pst.setString(5,dataStock);
          pst.setString(6,dataDescription);
          int row=pst.executeUpdate();
          
           JOptionPane.showMessageDialog(null,"Votre article a été créé avec succès");
        }catch(Exception e)
        {
            e.printStackTrace();
        }
         
    }//GEN-LAST:event_buttonAjoutActionPerformed

