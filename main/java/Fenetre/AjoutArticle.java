    private void textMarqueKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textMarqueKeyPressed
        // TODO add your handling code here:
         String marque=textMarque.getText();
        
        int tailleMarque=marque.length();
        
            if(tailleMarque<30){
                textMarque.setEditable(true);
            }else
            {
                  if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE ||evt.getExtendedKeyCode()==KeyEvent.VK_TAB) 
           {
               textMarque.setEditable(true);
           }else
           {
               textMarque.setEditable(false);
           }
            }
    }//GEN-LAST:event_textMarqueKeyPressed

